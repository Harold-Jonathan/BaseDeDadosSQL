IF DB_ID(N'BancoVendas') IS NULL
BEGIN
    CREATE DATABASE BancoVendas;
END
GO

USE BancoVendas;
GO

-- Remover tabelas na ordem correta por causa das chaves estrangeiras
IF OBJECT_ID(N'dbo.Pedidos', N'U') IS NOT NULL DROP TABLE dbo.Pedidos;
IF OBJECT_ID(N'dbo.Produtos', N'U') IS NOT NULL DROP TABLE dbo.Produtos;
IF OBJECT_ID(N'dbo.Clientes', N'U') IS NOT NULL DROP TABLE dbo.Clientes;
IF OBJECT_ID(N'dbo.Lojas', N'U') IS NOT NULL DROP TABLE dbo.Lojas;
IF OBJECT_ID(N'dbo.Locais', N'U') IS NOT NULL DROP TABLE dbo.Locais;
IF OBJECT_ID(N'dbo.Categorias', N'U') IS NOT NULL DROP TABLE dbo.Categorias;
GO

CREATE TABLE dbo.Categorias (
    ID_Categoria INT NOT NULL,
    Categoria NVARCHAR(100) NOT NULL,
    CONSTRAINT PK_Categorias PRIMARY KEY (ID_Categoria)
);
GO

CREATE TABLE dbo.Locais (
    Cidade NVARCHAR(100) NOT NULL,
    Estado CHAR(2) NOT NULL,
    Regiao NVARCHAR(50) NOT NULL,
    CONSTRAINT PK_Locais PRIMARY KEY (Cidade)
);
GO

CREATE TABLE dbo.Lojas (
    ID_Loja INT NOT NULL,
    Loja NVARCHAR(100) NOT NULL,
    Gerente NVARCHAR(150) NULL,
    Endereco NVARCHAR(300) NULL,
    Num_Funcionarios INT NULL,
    Telefone NVARCHAR(30) NULL,
    CONSTRAINT PK_Lojas PRIMARY KEY (ID_Loja),
    CONSTRAINT FK_Lojas_Locais FOREIGN KEY (Loja) REFERENCES dbo.Locais(Cidade)
);
GO

CREATE TABLE dbo.Produtos (
    ID_Produto INT NOT NULL,
    Nome_Produto NVARCHAR(200) NOT NULL,
    ID_Categoria INT NOT NULL,
    Marca_Produto NVARCHAR(100) NULL,
    Num_Serie NVARCHAR(50) NULL,
    Preco_Unit DECIMAL(10,2) NULL,
    Custo_Unit DECIMAL(10,2) NULL,
    CONSTRAINT PK_Produtos PRIMARY KEY (ID_Produto),
    CONSTRAINT FK_Produtos_Categorias FOREIGN KEY (ID_Categoria) REFERENCES dbo.Categorias(ID_Categoria)
);
GO

CREATE TABLE dbo.Clientes (
    ID_Cliente INT NOT NULL,
    Nome NVARCHAR(100) NOT NULL,
    Sobrenome NVARCHAR(100) NULL,
    Data_Nascimento DATE NULL,
    Estado_Civil CHAR(1) NULL,
    Sexo CHAR(1) NULL,
    Email NVARCHAR(150) NULL,
    Telefone NVARCHAR(30) NULL,
    Renda_Anual INT NULL,
    Qtd_Filhos INT NULL,
    Escolaridade NVARCHAR(100) NULL,
    CONSTRAINT PK_Clientes PRIMARY KEY (ID_Cliente)
);
GO

CREATE TABLE dbo.Pedidos (
    ID_Pedido INT NOT NULL,
    Data_Venda DATE NOT NULL,
    ID_Loja INT NOT NULL,
    ID_Produto INT NOT NULL,
    ID_Cliente INT NOT NULL,
    Qtd_Vendida INT NOT NULL,
    Receita_Venda DECIMAL(10,2) NULL,
    Custo_Venda DECIMAL(10,2) NULL,
    Custo_Unit DECIMAL(10,2) NULL,
    Preco_Unit DECIMAL(10,2) NULL,
    CONSTRAINT PK_Pedidos PRIMARY KEY (ID_Pedido),
    CONSTRAINT FK_Pedidos_Lojas FOREIGN KEY (ID_Loja) REFERENCES dbo.Lojas(ID_Loja),
    CONSTRAINT FK_Pedidos_Produtos FOREIGN KEY (ID_Produto) REFERENCES dbo.Produtos(ID_Produto),
    CONSTRAINT FK_Pedidos_Clientes FOREIGN KEY (ID_Cliente) REFERENCES dbo.Clientes(ID_Cliente)
);
GO

